"""Generate frozen paraphrases of the gold questions: the phrasing-robustness set.

Retrieval has repeatedly proven phrasing-sensitive ("appears on almost every team"
vs "most used"; chatty comparison phrasing diluting embeddings), and every such bug
was found by ad-hoc UI poking because the gold set tests exactly one phrasing per
fact. This asks Gemma for a few rewordings of each gold question and writes them to
eval/paraphrases.yaml, where run_eval scores each against the SAME expect_source as
its original. The file is meant to be generated once, skimmed, and committed:
regenerating is a deliberate act, otherwise the eval goes non-deterministic.

Guards: every Pokemon/move name (and gen9ou/gen2ou format marker) present in the
original must survive into a paraphrase verbatim-ish (squashed match), or the
paraphrase is dropped — a rewording that loses the entity tests description
resolution, not phrasing robustness. Follow-up (history) items are skipped: their
meaning lives in the transcript, and they already have their own eval row.

Run:  python -m eval.gen_paraphrases
"""
import asyncio
import json
import re
import sys
from pathlib import Path

import httpx
import yaml

sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "api"))
from config import settings          # noqa: E402
from router import find_entity_names  # noqa: E402

N_PER_QUESTION = 3

SYSTEM = """You reword questions. Given a question, produce alternative phrasings that a
different real user might type when asking for exactly the same information. Rules: keep
every Pokemon name, move name, item name, type name, and format name (like "Gen 9 OU")
exactly as written; change the sentence structure and the surrounding vocabulary; do not
add or remove constraints or entities; casual phrasing is fine. Output only JSON in the
form {"paraphrases": ["...", "..."]}."""


def _squash(s: str) -> str:
    return re.sub(r"[^a-z0-9]", "", s.lower())


def _required_terms(question: str) -> list[str]:
    ents = find_entity_names(question)
    req = ents["pokemon"] + ents["moves"]
    sq = _squash(question)
    req += [f for f in ("gen9ou", "gen2ou") if f in sq]
    return req


async def _paraphrase(client: httpx.AsyncClient, question: str) -> list[str]:
    prompt = f"Question: {question}\n\nWrite {N_PER_QUESTION} rewordings as JSON."
    r = await client.post("/api/generate", json={
        "model": settings.llm_model,
        "prompt": prompt,
        "system": SYSTEM,
        "stream": False,
        "format": "json",
        "think": False,
        "keep_alive": "10m",
    })
    r.raise_for_status()
    try:
        out = json.loads(r.json().get("response") or "{}").get("paraphrases") or []
    except json.JSONDecodeError:
        return []
    return [p.strip() for p in out if isinstance(p, str) and p.strip()]


async def main():
    qdir = Path(__file__).parent
    questions = yaml.safe_load((qdir / "questions.yaml").read_text())
    pfile = qdir / "paraphrases.yaml"
    # additive by default: questions already covered are skipped and existing file
    # text (including hand edits and comments) is preserved — the frozen set only
    # grows when new gold questions appear
    covered = set()
    if pfile.exists():
        covered = {e["question"] for e in (yaml.safe_load(pfile.read_text()) or [])}
    out = []
    dropped = 0

    async with httpx.AsyncClient(base_url=settings.ollama_host, timeout=120) as client:
        for item in questions:
            if item.get("history") or item["question"] in covered:
                continue
            q = item["question"]
            req = [_squash(t) for t in _required_terms(q)]
            kept = []
            for p in await _paraphrase(client, q):
                sq = _squash(p)
                if sq == _squash(q):
                    continue  # identical modulo punctuation: tests nothing
                if all(t in sq for t in req):
                    kept.append(p)
                else:
                    dropped += 1
                    print(f"  dropped (lost an entity): {p!r}")
            out.append({"question": q, "paraphrases": kept[:N_PER_QUESTION]})
            print(f"{q}\n  -> {kept[:N_PER_QUESTION]}")

    header = (
        "# Paraphrases of the gold questions, generated by eval/gen_paraphrases.py and\n"
        "# FROZEN by commit (regenerating is a deliberate act; a fresh sample would make\n"
        "# the eval non-deterministic). Each is scored against its original's\n"
        "# expect_source (or refusal, for no-answer originals) as the 'Paraphrase\n"
        "# hit-rate' eval row. Edit freely: drop bad rewordings, add real-user phrasings.\n"
    )
    if not out:
        print("\nNothing to do: every gold question is already covered.")
        return
    new_yaml = yaml.safe_dump(out, allow_unicode=True, sort_keys=False, width=100)
    if covered:
        # append below the existing text: hand edits and comments must survive
        pfile.write_text(pfile.read_text().rstrip() + "\n" + new_yaml)
    else:
        pfile.write_text(header + new_yaml)
    total = sum(len(e["paraphrases"]) for e in out)
    print(f"\nWrote {total} paraphrases for {len(out)} questions "
          f"({dropped} dropped by the entity guard) -> eval/paraphrases.yaml")


if __name__ == "__main__":
    asyncio.run(main())
